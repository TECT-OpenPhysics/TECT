class CurateStats:
    """Lightweight delta tracker so the operator sees what changed."""

    def __init__(self) -> None:
        self.added: list[str] = []
        self.modified: list[str] = []
        self.unchanged: int = 0
        self.removed: list[str] = []
        self.skipped_dotgit: int = 0


def _file_hash(p: Path) -> str:
    h = hashlib.sha256()
    with p.open("rb") as fh:
        for chunk in iter(lambda: fh.read(65536), b""):
            h.update(chunk)
    return h.hexdigest()


def _ensure_dir(p: Path) -> None:
    p.mkdir(parents=True, exist_ok=True)


def _write_text_idempotent(target: Path, content: str, stats: CurateStats,
                           rel_label: str, *, dry_run: bool) -> None:
    """Write `content` to `target`, comparing first to suppress noise.

    Always uses LF line-endings + UTF-8 to keep the public mirror diff
    stable across operator OS (the .gitattributes of the *public* repo
    is the operator's call; here we just normalise on emission)."""
    new_bytes = content.encode("utf-8")
    if target.exists():
        try:
            old_bytes = target.read_bytes()
        except OSError:
            old_bytes = None
        if old_bytes == new_bytes:
            stats.unchanged += 1
            return
        if not dry_run:
            target.write_bytes(new_bytes)
        stats.modified.append(rel_label)
        return
    if not dry_run:
        _ensure_dir(target.parent)
        target.write_bytes(new_bytes)
    stats.added.append(rel_label)


def _copy_file_idempotent(src: Path, dst: Path, stats: CurateStats,
                          rel_label: str, *, dry_run: bool) -> None:
    """Copy `src` to `dst` only if size+mtime differ (rsync-style fast skip).

    `shutil.copy2` preserves mtime + permissions, so after the first copy
    the destination has identical (size, mtime) to source. We rely on
    that for the fast path on subsequent runs. Content comparison is
    skipped because mtime preservation is sufficient when curate is the
    sole writer."""
    if dst.exists():
        try:
            ssz = src.stat().st_size
            smt = src.stat().st_mtime
            dsz = dst.stat().st_size
            dmt = dst.stat().st_mtime
            # 1-second granularity tolerance for FS that floor mtime.
            if ssz == dsz and abs(smt - dmt) < 1.0:
                stats.unchanged += 1
                return
            # Size differs OR mtime differs significantly — re-copy.
            if not dry_run:
                shutil.copy2(src, dst)
            stats.modified.append(rel_label)
            return
        except OSError:
            # Fall through to full copy on any stat error.
            pass
    if not dry_run:
        _ensure_dir(dst.parent)
        shutil.copy2(src, dst)
    stats.added.append(rel_label)


def _walk_files(root: Path) -> Iterable[Path]:
    """Walk `root` recursively, skipping anything under a `.git` directory.

    Sorted output for stable iteration across runs."""
    for dirpath, dirnames, filenames in os.walk(root):
        # Skip .git directories defensively (Github/ may have its own .git
        # once the push layer initialises it; we MUST NOT touch that tree).
        dirnames[:] = sorted(d for d in dirnames if d != ".git")
        for fn in sorted(filenames):
            yield Path(dirpath) / fn


