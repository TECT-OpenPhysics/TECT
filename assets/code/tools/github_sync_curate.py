def curate(args: argparse.Namespace) -> int:
    stats = CurateStats()
    dry_run = bool(args.check)

    # Math353 v2 preview short-circuit. Walks local sources, simulates
    # rename+rewrite, writes Docs/status/mirror-rewrite-preview.log,
    # exits 0. No Github/ writes. Safe regardless of toggle state.
    if getattr(args, "mirror_v2_preview", False):
        mirror_cfg = _load_mirror_v2_config()
        sources = [
            REPO_ROOT / "Codes",
            REPO_ROOT / "Docs",
            REPO_ROOT / "Runs",
            REPO_ROOT / "Website",
        ]
        log_path = REPO_ROOT / "Docs" / "status" / "mirror-rewrite-preview.log"
        summary = emit_v2_preview(mirror_cfg, sources, log_path)
        print("=" * 60)
        print(" Math353 v2 mirror preview (DRY-RUN)")
        print("=" * 60)
        print(f"  Files scanned   : {summary['files']}")
        print(f"  Renamed (path)  : {summary['renamed']}")
        print(f"  Content rewrites: {summary['rewrites_total']}")
        print(f"  Unreadable      : {summary['unreadable']}")
        print(f"  Toggle (v0_compat_disable_renames) = {mirror_cfg['v0_compat']}")
        print(f"  Log: {summary['log_path']}")
        print()
        print("This is a preview only; Github/ was NOT modified.")
        return 0

    # Math353 safety: if operator flips toggle to false WITHOUT going
    # through the B-delta cutover review, refuse to write.
    mirror_cfg_check = _load_mirror_v2_config()
    if not mirror_cfg_check["v0_compat"] and not dry_run:
        print("[ERROR] mirror.json::v0_compat_disable_renames=false detected, "
              "but the v2 production cutover (Math353 Phase B-delta) has not "
              "yet shipped. Run --mirror-v2-preview to view the v2 effect, "
              "or restore v0_compat_disable_renames=true to proceed with v0 "
              "publish.", file=sys.stderr)
        return 9

    if args.clean and not dry_run:
        # Wipe Github/ except .git/ (which the push layer owns).
        if TARGET.exists():
            for child in sorted(TARGET.iterdir()):
                if child.name == ".git":
                    stats.skipped_dotgit += 1
                    continue
                if child.is_dir():
                    shutil.rmtree(child)
                else:
                    child.unlink()
        else:
            _ensure_dir(TARGET)

    if not TARGET.exists():
        if not dry_run:
            _ensure_dir(TARGET)
            _ensure_dir(TARGET_DOCS)

    print("=" * 60)
    print(" GitHub publish — Layer 1 (curate)")
    print(f" Source: {WEBSITE.relative_to(REPO_ROOT)}")
    print(f" Target: {TARGET.relative_to(REPO_ROOT)}/")
    if dry_run:
        print(" Mode  : DRY-RUN (no files written)")
    print("=" * 60)

    # E1) Auto-generated top-level files
    print("[1/5] README + CITATION + LICENSE + .gitignore + .nojekyll …")
    _write_text_idempotent(TARGET / "README.md", render_readme(),
                           stats, "README.md", dry_run=dry_run)
    _write_text_idempotent(TARGET / "CITATION.cff", render_citation_cff(),
                           stats, "CITATION.cff", dry_run=dry_run)
    # LICENSE: now an auto-generated artefact (CC BY 4.0 since 2026-04-29).
    # Reproducibility > operator hand-edit. The legal code lives in
    # Codes/tools/templates/LICENSE-CC-BY-4.0.txt (verbatim official text).
    _write_text_idempotent(TARGET / "LICENSE", render_license(),
                           stats, "LICENSE", dry_run=dry_run)
    _write_text_idempotent(TARGET / ".gitignore", render_gitignore_for_target(),
                           stats, ".gitignore", dry_run=dry_run)
    _write_text_idempotent(TARGET / ".nojekyll", render_nojekyll(),
                           stats, ".nojekyll", dry_run=dry_run)

    # E2) Auto-generated docs/
    print("[2/5] docs/KEY_RESULTS + NAVIGATION + POLICIES_INDEX …")
    _ensure_dir(TARGET_DOCS) if not dry_run else None
    _write_text_idempotent(TARGET_DOCS / "KEY_RESULTS.md", render_key_results(),
                           stats, "docs/KEY_RESULTS.md", dry_run=dry_run)
    _write_text_idempotent(TARGET_DOCS / "NAVIGATION.md", render_navigation(),
                           stats, "docs/NAVIGATION.md", dry_run=dry_run)
    _write_text_idempotent(TARGET_DOCS / "POLICIES_INDEX.md", render_policies_index(),
                           stats, "docs/POLICIES_INDEX.md", dry_run=dry_run)

    # E3) Mirror Website/
    print("[3/5] Mirror Website/*.html + data/ + assets/ …")
    copy_website_tree(stats, dry_run=dry_run)

    # E4) Prune stale files
    print("[4/5] Prune stale files (anything in Github/ without a source) …")
    prune_stale_files(stats, dry_run=dry_run)

    # E5) Summary
    print("[5/5] Summary")
    print()
    print(f"  Added     : {len(stats.added)}")
    print(f"  Modified  : {len(stats.modified)}")
    print(f"  Unchanged : {stats.unchanged}")
    print(f"  Removed   : {len(stats.removed)}")
    if stats.skipped_dotgit:
        print(f"  Skipped (.git protected): {stats.skipped_dotgit}")

    if args.verbose:
        for label, items in (("Added", stats.added),
                             ("Modified", stats.modified),
                             ("Removed", stats.removed)):
            if items:
                print()
                print(f"  --- {label} ---")
                for it in items[:50]:
                    print(f"    {it}")
                if len(items) > 50:
                    print(f"    ... ({len(items) - 50} more)")

    if dry_run:
        print()
        print("DRY-RUN: no files were written. Re-run without --check to apply.")
    else:
        print()
        print("OK: Github/ is in sync with Website/ (Layer-1 contract satisfied).")
        print("Next step: run Codes/tools/github_sync_push.py once credentials")
        print("           are configured (see Docs/policy/GITHUB_SYNC_POLICY.md).")
    return 0


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--check", action="store_true",
                   help="dry-run: list deltas but do not write")
    p.add_argument("--clean", action="store_true",
                   help="wipe Github/ (preserving .git/) before rebuilding")
    p.add_argument("-v", "--verbose", action="store_true",
                   help="list per-file additions / modifications / removals")
    p.add_argument("--mirror-v2-preview", action="store_true",
                   help="Math353 B-gamma: dry-run v2 mirror (rename+rewrite); "
                        "emit Docs/status/mirror-rewrite-preview.log only. "
                        "Does NOT write to Github/. Safe regardless of toggle.")
    args = p.parse_args()
    return curate(args)


if __name__ == "__main__":
    sys.exit(main())
