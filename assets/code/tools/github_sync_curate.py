def _read_first_paragraph(p: Path) -> str:
    """Read first non-frontmatter paragraph of a markdown file."""
    if not p.exists():
        return ""
    text = p.read_text(encoding="utf-8", errors="replace")
    # Strip frontmatter
    text = re.sub(r"^---\n.*?\n---\n", "", text, count=1, flags=re.DOTALL)
    paragraphs = [s.strip() for s in re.split(r"\n\s*\n", text) if s.strip()]
    return paragraphs[0] if paragraphs else ""


def _parse_top_changelog(n: int = 5) -> list[Tuple[str, str]]:
    """Return `[(date, title), ...]` for the latest `n` CHANGELOG entries."""
    if not CHANGELOG.exists():
        return []
    text = CHANGELOG.read_text(encoding="utf-8", errors="replace")
    # CHANGELOG format observed: ## [title] — YYYY-MM-DD
    # Be lenient: also accept the alt form "## YYYY-MM-DD title".
    pat_a = re.compile(r"^##\s+\[(.+?)\]\s*[—\-]\s*(\d{4}-\d{2}-\d{2})\s*$",
                       re.MULTILINE)
    pat_b = re.compile(r"^##\s+(\d{4}-\d{2}-\d{2})\s+(.+)$", re.MULTILINE)
    out = []
    for m in pat_a.finditer(text):
        title = m.group(1).strip()
        date = m.group(2).strip()
        out.append((date, title))
        if len(out) >= n:
            return out
    if not out:
        for m in pat_b.finditer(text):
            out.append((m.group(1), m.group(2).strip()))
            if len(out) >= n:
                return out
    return out


